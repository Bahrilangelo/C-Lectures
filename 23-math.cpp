#include <stdio.h>
#include <math.h>
// We need the include math.h library

int main() {
	// ceil floor sqrt pow abs
	
	printf("%f\n", ceil(4.8)); // Round to the upper number
	printf("%f\n", floor(4.8)); // Round to the lower number
	printf("%f\n", sqrt(81)); // Takes the square root of a number
	printf("%f\n", pow(2,4)); // Take the exponent of a number
	printf("%d\n", abs(5)); // Gets the absolute value of the number

}