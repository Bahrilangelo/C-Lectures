#include <stdio.h>

int main() {
	// Assigment Operator
	// = += -= *= /= %=

	int x = 25;
	printf("%d\n", x);
	
	x += 3; // x = x + 3;
	printf("%d\n", x);

	x -= 3; // x = x - 3;
	printf("%d\n", x);

	x *= 2; // x = x * 2;
	printf("%d\n", x);

	x /= 5; // x = x / 5;
	printf("%d\n", x);

	x %= 3; // x = x % 3;
	printf("%d\n", x);



}