#include <stdio.h>

int main() {
	// Comparison Operator
	// == != > < <= >=

	int x = 25, y = 30;
	
	printf("%d\n", x == y); // is x equal to y

	printf("%d\n", x != y); // is x not equal to y

	printf("%d\n", x > y); // is x greater than y

	printf("%d\n", x < y); // is x lower than y

	printf("%d\n", x >= y); // is x greater or equal to y

	printf("%d", x <= y); // is x lower or equal to y


}